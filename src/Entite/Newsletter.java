package Entite;

import java.util.Objects;

/**
 *
 * @author said hmidi
 */
public class Newsletter implements java.io.Serializable  {
    
     private Integer id; 
    private String name;    
    private String email; 

    public Newsletter(Integer id, String name, String email) {
        this.id = id;
        this.name = name;
        this.email = email;
    }
    public Newsletter(String name, String email) {
        this.name = name;
        this.email = email;
    }

    public Newsletter() {
    }

    @Override
    public String toString() {
        return "Newsletter{" + "id=" + id + ", name=" + name + ", email=" + email + '}';
    }
    

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 97 * hash + Objects.hashCode(this.id);
        hash = 97 * hash + Objects.hashCode(this.name);
        hash = 97 * hash + Objects.hashCode(this.email);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Newsletter other = (Newsletter) obj;
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        if (!Objects.equals(this.email, other.email)) {
            return false;
        }
        if (!Objects.equals(this.id, other.id)) {
            return false;
        }
        return true;
    }
    
    
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
    
    
    
    
}
